    public double []comprobar(double m[][],int o,int d){
        double v[]=new double[4];
        double so=0,sd=0;
        for(int i=0;i<o;i++){
            so=so+m[i][d-1];
        }
        for(int i=0;i<d;i++){
            sd=sd+m[o-1][i];
        }
        if(sd!=so){
            //145  45
            if(sd>so){
                o=o+1;//3
            }else{
                d=d+1;//2
            }
        }
        if(sd!=so){
            v[0]=o;
            v[1]=d;
            v[2]=so;
            v[3]=sd;
        }else{
            v[0]=o;
            v[1]=d;
            v[2]=0;
            v[3]=0;
        }
        return v;
    }
