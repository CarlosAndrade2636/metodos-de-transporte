    public void operacion(double m1[][],int o,int d){
        double b[][]=new double[o][d];
        for(int i=0;i<o;i++){
            b[i][d-1]=m1[i][d-1];
        }
         for(int i=0;i<d;i++){
            b[o-1][i]=m1[o-1][i];
        }/*
         for(int i=0;i<o;i++){
             for(int j=0;j<d;j++){
             System.out.print(b[i][j]+" ");
             }System.out.println();
         }*/
        int pos=0;int c2=0;
        for(int i=0;i<o;i++){
            for(int j=pos;j<d;j++){
                if(b[i][d-1]==b[o-1][j]){
                    b[i][j]=b[i][d-1];
                    b[i][d-1]=0;
                    b[o-1][j]=0;
                    pos=j;c2=1;
                    break;
                }else{
                    if(b[i][d-1]<b[o-1][j]){
                             
                        b[o-1][j]=b[o-1][j]-b[i][d-1];
                        b[i][j]=b[i][d-1];
                        b[i][d-1]=0;
                        pos=j;
                        break;
                    }else{
                        b[i][d-1]=b[i][d-1]-b[o-1][j];
                        b[i][j]=b[o-1][j];
                        b[o-1][j]=0;
                    }
                    
                }
            }
            if(c2==1){break;}
        }
        DefaultTableModel model = (DefaultTableModel) tabla3.getModel();
        model.setRowCount(o);
        model.setColumnCount(d);
        double su=0;
        for(int i=0;i<o;i++){
            for(int j=0;j<d;j++){
             if(b[i][j]!=0){
                    
             tabla3.setValueAt("[ "+b[i][j]+" , "+m1[i][j]+" ]", i, j);
             su=su+b[i][j]*m1[i][j];
            }else{
             tabla3.setValueAt(" - ", i, j);
             }
            }
        }
        label.setText("Costo Total: "+su);
    }
